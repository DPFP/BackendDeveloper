public class ScratchPad {
    public static void main(String[] args) {

        // Test ++ --
        int i = 10;
        System.out.println(i++); // 10
        i = 10;
        System.out.println(++i); // 11
        i = 10;
        System.out.println(i--); // 10
        i = 10;
        System.out.println(--i); // 9
    }
}
